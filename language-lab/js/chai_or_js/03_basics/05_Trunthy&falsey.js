// In JavaScript, the robot likes to sort absolutely everything into two big buckets: The "Something" Bucket (True) and The "Nothing" Bucket (False).

// Developers call this "Truthy" and "Falsy".

// The "Nothing" Bucket (Falsy values):
// These are things that feel empty or broken. There are only a few of them in the whole language:

// "" (An empty word/string. Like an empty toy box.)

// 0 (The number zero. Like having zero toys.)

// undefined (Meaning you never even got a toy box.)

// null (Meaning you got a box, but it was intentionally left empty.)

// NaN (Not a Number - like trying to do math with a banana.)

// false (The actual word false.)

// The "Something" Bucket (Truthy values):
// Literally everything else. If it’s not in the list above, JavaScript considers it "True". "apple", 50, [] (an array)—these are all "Something".
